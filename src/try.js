import Bilza, { hsl } from "./bilza.js";
import Ui from "./ui/ui.js";
const bil = new Bilza("bilza");
bil.background.color.set(hsl(60, 30, 85));
bil.alwaysOn().canvasBorder(hsl(0, 70, 20), 0.5);
const grid = bil.alwaysOn().grid();

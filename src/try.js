import Bilza, { hsl } from "./bilza.js";
import Ui from "./ui/ui.js";
const bil = new Bilza("bilza");
bil.background.color.set(hsl(240, 60, 30));
bil.alwaysOn().canvasBorder(hsl(0, 70, 20), 0.5);
const grid = bil.alwaysOn().grid();
const comp = bil.add(0, 60).curve(100, 0, 0, 50, 100, 100);
comp.color.set("red");
comp.lineWidth.set(5);
comp.showBackground.set(true);
comp.colorBackground.set(hsl(0, 20, 60));
comp.setPaddings(0);
comp.width.animate(0, 5, 30, 60);
comp.height.animate(6, 10, 30, 60);
comp.goto(12, 20, 20);
bil.draw();
console.log("bil", bil);
const ui = new Ui(bil.bil);

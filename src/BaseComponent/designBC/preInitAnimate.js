export default class PreInitAnimate {
    constructor(timeFrom, timeTo, xFrom, xTo, yFrom, yTo, xExtraFrom = 0, xExtraTo = 0, yExtraFrom = 0, yExtraTo = 0) {
        this.timeFrom = timeFrom;
        this.timeTo = timeTo;
        this.xFrom = xFrom;
        this.xTo = xTo;
        this.yFrom = yFrom;
        this.yTo = yTo;
        this.xExtraFrom = xExtraFrom;
        this.xExtraTo = xExtraTo;
        this.yExtraFrom = yExtraFrom;
        this.yExtraTo = yExtraTo;
    }
}

import Bilza, { XAlignment, YAlignment, CompFactory as cf } from "../Bilza.js";
let bil = new Bilza("bilza");
bil.resizeCanvas(1000, 400);
const g = cf.grid();
bil.insert.alwaysOn(g);
let tst = cf.text("123-123");
tst.duration = 500;
tst.showBg = true;
tst.colorBg = "crimson";
tst.color = "#ffffff";
tst.fontSize = 100;
tst.loc.goto(0, 50, 50, XAlignment.Mid, YAlignment.Mid);
bil.insert.append(tst, tst.duration);
bil.init();
bil.start();

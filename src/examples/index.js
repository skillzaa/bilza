import Bilza, { XAlignment, YAlignment, CompFactory as cf } from "../Bilza.js";
let bil = new Bilza("bilza");
bil.resizeCanvas(1000, 400);
let grid = cf.grid();
bil.insert.alwaysOn(grid);
let tst = cf.rect();
tst.duration = 600;
tst.p.loc.animate(3, 6, 0, 50, 0, 1, XAlignment.Left, XAlignment.Left, YAlignment.Top, YAlignment.Top);
tst.p.loc.goto(0, 90, 10, XAlignment.Mid, YAlignment.Mid);
bil.insert.append(tst, tst.duration);
bil.init();
bil.start();
